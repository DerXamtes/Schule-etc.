public class Cola {
    public static void main(String[] args) {
 
        int a = 10;
        while (a > 0) {

            if (a > 0) {

                System.out.println(a + " Flaschen Cola im Kühlschrank.");
            
            } else {

                System.out.println("Eine Flasche Cola im Kühlschrank.");
            }

        a--;
        }
    
    }

}